@router.get("/attachments", response_model=AttachmentListResponse)
async def list_attachments(
    service: Annotated[AttachmentService, Depends(get_attachment_service)],
    claims=Depends(get_current_access_claims),
    entity_type: str | None = Query(default=None, description="Entity type (task, risk, control, etc.)"),
    entity_id: str | None = Query(default=None, description="UUID of the entity"),
    engagement_id: str | None = Query(default=None, description="Optional: Scope all evidence for an engagement"),
    page: int = Query(default=1, ge=1),
    per_page: int = Query(default=50, ge=1, le=200),
) -> AttachmentListResponse:
    """List all non-deleted attachments for a given entity or engagement. Returns metadata only."""
    if not engagement_id and (not entity_type or not entity_id):
        raise ValidationError("Either engagement_id or both entity_type and entity_id must be provided.")

    effective_tenant_key = claims.tenant_key
    auditor_only = False
    viewer_membership_id: str | None = None

    if not is_assignee_portal_mode(claims.portal_mode):
        async with service._database_pool.acquire() as conn:
            if engagement_id:
                effective_tenant_key, auditor_only, viewer_membership_id = await _resolve_engagement_attachment_scope(
                    conn,
                    engagement_id,
                    claims,
                )
            else:
                await require_permission(conn, claims.subject, "attachments.view")
    return await service.list_attachments(
        user_id=claims.subject,
        tenant_key=effective_tenant_key,
        portal_mode=claims.portal_mode,
        entity_type=entity_type,
        entity_id=entity_id,
        engagement_id=engagement_id,
        auditor_only=auditor_only,
        viewer_membership_id=viewer_membership_id,
        page=page,
        per_page=per_page,
    )


