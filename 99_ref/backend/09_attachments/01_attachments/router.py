async def _get_user_email(conn, user_id: str, *, is_api_key: bool) -> str | None:
    if is_api_key:
        return "admin@test.com"
    return await conn.fetchval(
        'SELECT property_value FROM "03_auth_manage"."05_dtl_user_properties" WHERE user_id = $1::uuid AND property_key = \'email\'',
        user_id,
    )


async def _resolve_engagement_attachment_scope(conn, engagement_id: str, claims) -> tuple[str, bool, str | None]:
    service = _get_engagement_service()
    if not await service.is_user_globally_active(conn, user_id=claims.subject):
        raise AuthorizationError("User account is inactive or suspended.")

    owner_eng = await service.get_engagement(conn, engagement_id, claims.tenant_key)
    if owner_eng:
        try:
            await require_permission(conn, claims.subject, "attachments.view")
            has_access = await check_engagement_access(
                conn,
                user_id=claims.subject,
                org_id=owner_eng.org_id,
                engagement_id=engagement_id,
            )
            if has_access:
                return claims.tenant_key, False, None
        except AuthorizationError:
            pass

    membership_access = await service.get_active_membership_access(
        conn,
        engagement_id=engagement_id,
        user_id=claims.subject,
    )
    if membership_access:
        return str(membership_access["tenant_key"]), True, str(membership_access["membership_id"])

    user_email = await _get_user_email(conn, claims.subject, is_api_key=claims.is_api_key)
    if user_email:
        guest_tenant = await service.validate_auditor_access_and_get_tenant(conn, engagement_id, user_email)
        if guest_tenant:
            return guest_tenant, True, None

    raise AuthorizationError("You do not have access to this engagement evidence.")


async def _assert_attachment_engagement_boundary(conn, service: AttachmentService, attachment_id: str, claims) -> None:
    record = await service._repository.get_attachment_by_id(conn, attachment_id)
    if record is None:
        raise ValidationError("Attachment not found.")
    engagement_service = _get_engagement_service()
    if not await engagement_service.is_user_globally_active(conn, user_id=claims.subject):
        raise AuthorizationError("User account is inactive or suspended.")

    contexts = await service._repository.list_attachment_engagement_contexts(conn, attachment_id)
    if not contexts:
        return

    user_email = await _get_user_email(conn, claims.subject, is_api_key=claims.is_api_key)

    for context in contexts:
        engagement_id = context["engagement_id"]

        owner_eng = await engagement_service.get_engagement(conn, engagement_id, claims.tenant_key)
        if owner_eng:
            try:
                await require_permission(conn, claims.subject, "attachments.view")
                has_access = await check_engagement_access(
                    conn,
                    user_id=claims.subject,
                    org_id=owner_eng.org_id,
                    engagement_id=engagement_id,
                )
                if has_access:
                    return
            except AuthorizationError:
                pass

        membership_access = await engagement_service.get_active_membership_access(
            conn,
            engagement_id=engagement_id,
            user_id=claims.subject,
        )
        if membership_access:
            has_grant = await service._repository.has_active_creator_approved_evidence_grant(
                conn,
                engagement_id=engagement_id,
                membership_id=str(membership_access["membership_id"]),
                attachment_id=attachment_id,
            )
            if has_grant:
                return

    raise AuthorizationError("You do not have access to this attachment.")


async def _trigger_evidence_check_for_task(
    pool,
    tenant_key: str,
    org_id: str | None,
    task_id: str,
    triggered_by_attachment_id: str | None,
    user_id: str,
) -> None:
    """Fire-and-forget: enqueue evidence check after a task attachment upload."""
    try:
        _jh = import_module("backend.20_ai.16_evidence_checker.job_handler")
        # Load current attachment IDs for the task
        async with pool.acquire() as conn:
            rows = await conn.fetch(
                """
                SELECT id::text
                FROM "09_attachments"."01_fct_attachments"
                WHERE entity_type = 'task' AND entity_id = $1::uuid AND is_deleted = FALSE
                """,
                task_id,
            )
        attachment_ids = [r["id"] for r in rows]
        if not attachment_ids:
            return
        # enqueue_evidence_check expects a raw asyncpg.Pool
        raw_pool = pool.pool if hasattr(pool, "pool") else pool
        await _jh.enqueue_evidence_check(
            pool=raw_pool,
            tenant_key=tenant_key,
            org_id=org_id or "",
            task_id=task_id,
            triggered_by_attachment_id=triggered_by_attachment_id,
            attachment_ids=attachment_ids,
            user_id=user_id,
        )
    except Exception as exc:
        _router_logger.warning(
            "evidence_check.auto_trigger_failed: %s", exc,
            extra={"task_id": task_id},
        )


def _get_client_ip(request: Request) -> str | None:
    """Extract client IP, respecting trusted proxy headers when configured."""
    forwarded_for = request.headers.get("X-Forwarded-For")
    if forwarded_for:
        return forwarded_for.split(",")[0].strip()
    return request.client.host if request.client else None


