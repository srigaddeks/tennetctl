# POST /attachments/upload — upload a single attachment (backward compatible)
# NOTE: must be before /attachments/{id} to prevent routing shadow
# ---------------------------------------------------------------------------

@router.post(
    "/attachments/upload",
    response_model=UploadAttachmentResponse,
    status_code=status.HTTP_201_CREATED,
)
async def upload_attachment(
    service: Annotated[AttachmentService, Depends(get_attachment_service)],
    claims=Depends(get_current_access_claims),
    file: UploadFile = ...,
    entity_type: str = Form(...),
    entity_id: str = Form(...),
    org_id: str | None = Form(default=None),
    workspace_id: str | None = Form(default=None),
    description: str | None = Form(default=None),
) -> UploadAttachmentResponse:
    """Upload a single file attachment to the configured object storage provider.

    - Validates file size against STORAGE_MAX_FILE_SIZE_MB
    - Validates content type against the allowed list
    - Computes SHA-256 checksum for integrity verification
    - Uploads to object storage BEFORE writing to the database
    - On DB failure, automatically removes the uploaded object
    - ``org_id`` and ``workspace_id`` are used to build the hierarchical storage key
    """
    # Verify user has permission to create attachments on this entity
    if not is_assignee_portal_mode(claims.portal_mode):
        async with service._database_pool.acquire() as conn:
            await require_permission(conn, claims.subject, "attachments.create")

    file_data, _checksum = await AttachmentService._read_file_chunked(file)
    original_filename = file.filename or "attachment"
    declared_content_type = file.content_type or "application/octet-stream"

    result = await service.upload_attachment(
        user_id=claims.subject,
        tenant_key=claims.tenant_key,
        portal_mode=claims.portal_mode,
        entity_type=entity_type,
        entity_id=entity_id,
        description=description,
        original_filename=original_filename,
        declared_content_type=declared_content_type,
        file_data=file_data,
        org_id=org_id,
        workspace_id=workspace_id,
    )

    # Auto-trigger evidence check for task attachments (fire and forget)
    if entity_type == "task":
        asyncio.create_task(
            _trigger_evidence_check_for_task(
                pool=service._database_pool,
                tenant_key=claims.tenant_key,
                org_id=org_id,
                task_id=entity_id,
                triggered_by_attachment_id=result.id,
                user_id=claims.subject,
            )
        )

    return result


