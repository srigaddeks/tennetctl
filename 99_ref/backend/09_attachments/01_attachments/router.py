# GET /attachments/{id}/download — get presigned download URL
# NOTE: must be before /attachments/{id} to avoid routing collision
# ---------------------------------------------------------------------------

@router.get("/attachments/{attachment_id}/download", response_model=PresignedDownloadResponse)
async def get_download_url(
    attachment_id: str,
    request: Request,
    service: Annotated[AttachmentService, Depends(get_attachment_service)],
    claims=Depends(get_current_access_claims),
) -> PresignedDownloadResponse:
    """Generate a presigned download URL valid for 1 hour.

    Records a download event with client IP and user agent for audit purposes.
    Files are never proxied through the backend — clients download directly
    from object storage using the presigned URL.
    """
    # Verify user has permission to view/download attachments
    async with service._database_pool.acquire() as conn:
        if not is_assignee_portal_mode(claims.portal_mode):
            await require_permission(conn, claims.subject, "attachments.view")
        await _assert_attachment_engagement_boundary(conn, service, attachment_id, claims)

    client_ip = _get_client_ip(request)
    user_agent = request.headers.get("User-Agent")

    return await service.get_download_url(
        user_id=claims.subject,
        attachment_id=attachment_id,
        portal_mode=claims.portal_mode,
        client_ip=client_ip,
        user_agent=user_agent,
    )


