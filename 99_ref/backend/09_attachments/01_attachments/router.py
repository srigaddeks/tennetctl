# DELETE /attachments/{id} — soft delete + storage removal
# ---------------------------------------------------------------------------

@router.delete("/attachments/{attachment_id}", status_code=status.HTTP_204_NO_CONTENT)
async def delete_attachment(
    attachment_id: str,
    service: Annotated[AttachmentService, Depends(get_attachment_service)],
    claims=Depends(get_current_access_claims),
) -> None:
    """Soft-delete an attachment and remove it from object storage.

    Only the original uploader or a user with the attachments.delete permission may delete.
    """
    is_uploader = await service.is_uploader(
        user_id=claims.subject, attachment_id=attachment_id
    )
    if not is_uploader:
        async with service._database_pool.acquire() as conn:
            await require_permission(conn, claims.subject, "attachments.delete")

    await service.delete_attachment(
        user_id=claims.subject,
        attachment_id=attachment_id,
        portal_mode=claims.portal_mode,
    )
