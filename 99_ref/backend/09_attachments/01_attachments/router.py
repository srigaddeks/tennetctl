# POST /attachments/bulk-upload — upload up to 10 files in one request
# NOTE: must be before /attachments/{id} to prevent routing shadow
# ---------------------------------------------------------------------------

@router.post(
    "/attachments/bulk-upload",
    response_model=BulkUploadResponse,
    status_code=status.HTTP_207_MULTI_STATUS,
)
async def bulk_upload_attachments(
    service: Annotated[AttachmentService, Depends(get_attachment_service)],
    claims=Depends(get_current_access_claims),
    files: list[UploadFile] = ...,
    entity_type: str = Form(...),
    entity_id: str = Form(...),
    org_id: str | None = Form(default=None),
    workspace_id: str | None = Form(default=None),
    description: str | None = Form(default=None),
) -> BulkUploadResponse:
    """Upload up to 10 files in a single request.

    Each file is processed independently — partial success is possible.
    Returns HTTP 207 Multi-Status with per-file results.

    Successful files are in `uploaded`; files that could not be processed are
    in `failed` with a human-readable `error` message.
    """
    # Verify user has permission to create attachments
    if not is_assignee_portal_mode(claims.portal_mode):
        async with service._database_pool.acquire() as conn:
            await require_permission(conn, claims.subject, "attachments.create")

    if len(files) > _MAX_BULK_FILES:
        raise ValidationError(
            f"Too many files. A maximum of {_MAX_BULK_FILES} files may be uploaded at once."
        )

    file_payloads = []
    for upload_file in files:
        file_data, _checksum = await AttachmentService._read_file_chunked(upload_file)
        file_payloads.append({
            "filename": upload_file.filename or "attachment",
            "content_type": upload_file.content_type or "application/octet-stream",
            "data": file_data,
            "description": description,
        })

    result = await service.bulk_upload_attachments(
        user_id=claims.subject,
        tenant_key=claims.tenant_key,
        portal_mode=claims.portal_mode,
        entity_type=entity_type,
        entity_id=entity_id,
        files=file_payloads,
        org_id=org_id,
        workspace_id=workspace_id,
    )

    # Auto-trigger evidence check for task attachments when at least one file succeeded
    if entity_type == "task" and result.total_uploaded > 0:
        first_id = result.uploaded[0].id if result.uploaded else None
        asyncio.create_task(
            _trigger_evidence_check_for_task(
                pool=service._database_pool,
                tenant_key=claims.tenant_key,
                org_id=org_id,
                task_id=entity_id,
                triggered_by_attachment_id=first_id,
                user_id=claims.subject,
            )
        )

    return result


